package com.hejia.entity;

public class AirFanCondition {
    private int frontAirFan = 0;//前换气扇
    private int behindAirFan = 0;//后换气扇
    private int driverAirFan = 0;//司机换气扇



    public int getFrontAirFan() {
        return frontAirFan;
    }
    public void setFrontAirFan(int frontAirFan) {
        this.frontAirFan = frontAirFan;
    }
    public int getBehindAirFan() {
        return behindAirFan;
    }
    public void setBehindAirFan(int behindAirFan) {
        this.behindAirFan = behindAirFan;
    }
    public int getDriverAirFan() {
        return driverAirFan;
    }
    public void setDriverAirFan(int driverAirFan) {
        this.driverAirFan = driverAirFan;
    }
}
