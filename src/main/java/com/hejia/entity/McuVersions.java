package com.hejia.entity;

public class McuVersions {

    private int hardwareMainVers = 0;				//硬件主版本号
    private int hardwareSonVers = 0; 			  //硬件子版本号
    private int hardwareReviseVers = 0;					//硬件修订版本号
    private int softwareMainVers = 0;		//软件主版本号
    private int softwareSonVers = 0;		//软件子版本号
    private int softwareReviseVers = 0;		//软件修订版本号
	
	
    public int getHardwareMainVers() {
        return hardwareMainVers;
    }
    public void setHardwareMainVers(int hardwareMainVers) {
        this.hardwareMainVers = hardwareMainVers;
    }
    public int getHardwareSonVers() {
        return hardwareSonVers;
    }
    public void setHardwareSonVers(int hardwareSonVers) {
        this.hardwareSonVers = hardwareSonVers;
    }
    public int getHardwareReviseVers() {
        return hardwareReviseVers;
    }
    public void setHardwareReviseVers(int hardwareReviseVers) {
        this.hardwareReviseVers = hardwareReviseVers;
    }
    public int getSoftwareMainVers() {
        return softwareMainVers;
    }
    public void setSoftwareMainVers(int softwareMainVers) {
        this.softwareMainVers = softwareMainVers;
    }
    public int getSoftwareSonVers() {
        return softwareSonVers;
    }
    public void setSoftwareSonVers(int softwareSonVers) {
        this.softwareSonVers = softwareSonVers;
    }
    public int getSoftwareReviseVers() {
        return softwareReviseVers;
    }
    public void setSoftwareReviseVers(int softwareReviseVers) {
        this.softwareReviseVers = softwareReviseVers;
    }

}
